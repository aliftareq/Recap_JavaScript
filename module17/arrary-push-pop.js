//array push and pop operation


//array push(adding in last index)
var numbers = [78, 45, 98, 45];
numbers.push(63)
console.log(numbers);

var friends = ['balam', 'kalam', 'salam']
friends.push('gelam')
friends.push('pailam')
console.log(friends);


//array pop(removing from last index)
var ages = [11, 12, 13, 14];
ages.pop()
console.log(ages);

var element = ages.pop()
console.log(element);

//array shift(adding in first index)
friends.unshift('alif')
console.log(friends);


//array unshift(removing from last index)
ages.shift()
console.log(ages);